//Booleanos são tipos de dados que retornam true ou false.

//Maior que
//Verifica se um valor é maior que outro. Se sim, retorna true. Se não for, retorna false.
console.log('5 > 5', 5 > 5);
console.log('10 > 3', 10 > 3);

//Menor que
//Verifica se um valor é menor que outro.
console.log('5 < 5', 5 < 5);
console.log('5 < 7', 5 < 7);

//Maior ou igual
//Verifica se um valor é maior ou igual a outro
console.log('5 >= 5', 5 >= 5);

//Menor ou igual
//Verifica se um valor é menor ou igual a outro
console.log('15 <= 20', 15 <= 20);

//Igual
//Verifica se um valor é igual a outro ou não
console.log('5 == 5', 5 == 5);
//Existe a possibilidade de verificar a igualdade utilzando 3 sinais de = ao invés de 2. Utilizando 2, ele somente verifica se os valores são iguais, independente se são tipos diferentes de dados. Já com 3 sinais de igual, ele verifica se os valores e se os tipos dos dados são iguais.

//Diferente
//Verifica se um valor é diferente de outro
console.log('10 !== 12', 10 !== 12);