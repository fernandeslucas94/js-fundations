// Podemos entender objeto no JS, como um objeto de verdade, físico. Contém caracteríticas, atributos, pode ser bem utilizado quando necessário, etc...

// Em um objeto, podemos podemos guardar informações acerca de algo que necessitamos, e que posteriormente poderemos utilizar.

// Um exemplo é um objeto pessoa:

let pessoa = {
    nome: 'Lucas',
    sobrenome: 'Fernandes',
    idade: 26,
    nacionalidade: 'Brasileiro',
    naturalidade: 'Itabirinha',
    estado_de_nascimento: 'Minas Gerais'
}

// Note que o objeto é uma variável, que recebe diversos atributos como valor. Diferente do Array, após os nomes dos atributos, devemos adicionar dois pontos e em seguida o valor que queremos. Após definirmos o valor, caso haja mais de um, devemos colocar uma vírgula e seguir para o seguinte ítem ou atributo.